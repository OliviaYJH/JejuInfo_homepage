// 내용 자세히 보기 및 닫기
function display1(){
    var dom = document.getElementById("no1");
    var button = document.getElementById("no1button");

    if(dom.style.display == 'none'){
        dom.style.display = 'block';
        button.value="닫기";
    }
    else{
        dom.style.display = 'none';
        button.value="자세히보기";
    }
}

